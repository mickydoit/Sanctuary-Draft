// Generates supabase-schema.sql from the SAME data + fixture-generation code the
// Node app used, so the static site's seed is identical to the original seed.
// Run with: node scripts/gen-supabase-sql.mjs
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { TEAMS, DEFAULT_PLAYERS } from '../src/data/teams.js';
import { buildGroupFixtures } from '../src/data/schedule2026.js';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Teams get ids 1..48 in ranking order (same order the original seed inserted them).
// Group fixtures use the REAL 2026 schedule (pairings, dates, kick-off times),
// already sorted chronologically by buildGroupFixtures. Ids 1..72 follow kickoff order.
const fixtureRows = buildGroupFixtures(TEAMS).map((fx, i) => ({ id: i + 1, ...fx }));

const esc = (s) => (s == null ? 'NULL' : `'${String(s).replace(/'/g, "''")}'`);

const lines = [];
lines.push(`-- LBH Club — World Cup Draft : Supabase schema + seed (generated).
-- Paste this whole file into the Supabase SQL Editor and run it once.
-- It is idempotent-ish: it drops and recreates the app tables, so re-running
-- RESETS everything (teams, players, fixtures, draft, scores). Run it again any
-- time you want a clean slate before the real draft.

drop table if exists nominations cascade;
drop table if exists picks cascade;
drop table if exists fixtures cascade;
drop table if exists teams cascade;
drop table if exists players cascade;
drop table if exists settings cascade;

create table players (
  id          integer primary key,
  name        text not null,
  draft_slot  integer
);

create table teams (
  id       integer primary key,
  name     text not null unique,
  code     text,
  grp      text,
  ranking  integer,
  api_id   text
);

create table fixtures (
  id             bigint generated by default as identity primary key,
  api_id         text unique,
  stage          text not null,          -- group,R32,R16,QF,SF,third,final
  grp            text,
  matchday       integer,
  kickoff        timestamptz,
  home_team_id   integer references teams(id),
  away_team_id   integer references teams(id),
  home_score     integer,
  away_score     integer,
  winner_team_id integer references teams(id),
  status         text not null default 'scheduled', -- scheduled,live,finished
  updated_at     timestamptz default now()
);

create table picks (
  id           bigint generated by default as identity primary key,
  pick_number  integer not null unique,
  round        integer not null,
  player_id    integer not null references players(id),
  team_id      integer not null references teams(id) unique,
  created_at   timestamptz default now()
);

create table nominations (
  fixture_id  bigint not null references fixtures(id) on delete cascade,
  player_id   integer not null references players(id),
  team_id     integer not null references teams(id),
  primary key (fixture_id, player_id)
);

create table settings (
  id                 integer primary key default 1,
  draft_status       text not null default 'not_started', -- not_started,in_progress,complete
  draft_started_at   timestamptz,
  score_third_place  boolean not null default true,
  constraint settings_singleton check (id = 1)
);
`);

// Seed data ---------------------------------------------------------------
lines.push(`\n-- ---- seed: settings ----`);
lines.push(`insert into settings (id) values (1);`);

lines.push(`\n-- ---- seed: players ----`);
DEFAULT_PLAYERS.forEach((name, i) => {
  lines.push(`insert into players (id, name) values (${i + 1}, ${esc(name)});`);
});

lines.push(`\n-- ---- seed: 48 teams (ranking order) ----`);
TEAMS.forEach((t, i) => {
  lines.push(
    `insert into teams (id, name, code, grp, ranking) values (${i + 1}, ${esc(t.name)}, ${esc(t.code)}, ${esc(t.grp)}, ${t.ranking});`
  );
});

lines.push(`\n-- ---- seed: ${fixtureRows.length} group fixtures ----`);
fixtureRows.forEach((f) => {
  lines.push(
    `insert into fixtures (id, stage, grp, matchday, kickoff, home_team_id, away_team_id) values (${f.id}, 'group', ${esc(f.grp)}, ${f.matchday}, ${esc(f.kickoff)}, ${f.homeTeamId}, ${f.awayTeamId});`
  );
});

// Keep the identity sequence ahead of the seeded fixture ids so app inserts
// (knockout fixtures added later) don't collide.
lines.push(`\n-- keep identity counters ahead of seeded ids`);
lines.push(`select setval(pg_get_serial_sequence('fixtures', 'id'), ${fixtureRows.length});`);

// Row Level Security: this is a private 5-mate pool. The anon key is public, so
// we allow read+write to it (the UI gates writes behind the admin password).
lines.push(`\n-- ---- row level security (open read+write for the shared anon key) ----`);
for (const t of ['players', 'teams', 'fixtures', 'picks', 'nominations', 'settings']) {
  lines.push(`alter table ${t} enable row level security;`);
  lines.push(`drop policy if exists "open access" on ${t};`);
  lines.push(`create policy "open access" on ${t} for all using (true) with check (true);`);
  lines.push(`grant all on table ${t} to anon, authenticated;`);
}
lines.push(`grant usage, select on all sequences in schema public to anon, authenticated;`);
lines.push('');

writeFileSync(join(__dirname, '..', 'supabase-schema.sql'), lines.join('\n'));
console.log(`Wrote supabase-schema.sql (${TEAMS.length} teams, ${DEFAULT_PLAYERS.length} players, ${fixtureRows.length} fixtures).`);

// ---------------------------------------------------------------------------
// Non-destructive fixtures-only migration.
// Use this (NOT the full schema) to correct the schedule on a live database
// that already has a draft: it replaces only the group fixtures and leaves
// teams, players, picks, nominations and settings untouched.
// ---------------------------------------------------------------------------
const fix = [];
fix.push(`-- LBH Club — World Cup Draft : fix group fixtures (NON-DESTRUCTIVE).
-- Paste this whole file into the Supabase SQL Editor and run it once.
--
-- It replaces ONLY the group-stage fixtures with the real 2026 schedule.
-- Your teams, players, PICKS (the completed draft), nominations and settings
-- are left completely untouched. Knockout fixtures (if any have been added
-- later) are also preserved.

begin;

-- Out with the old placeholder group fixtures (nominations cascade, but there
-- are none pre-tournament; picks reference teams, not fixtures, so are safe).
delete from fixtures where stage = 'group';

-- In with the 72 real group fixtures (ids 1..${fixtureRows.length}, chronological).`);
fix.push(
  `insert into fixtures (id, stage, grp, matchday, kickoff, home_team_id, away_team_id) values`
);
fix.push(
  fixtureRows
    .map(
      (f) =>
        `  (${f.id}, 'group', ${esc(f.grp)}, ${f.matchday}, ${esc(f.kickoff)}, ${f.homeTeamId}, ${f.awayTeamId})`
    )
    .join(',\n') + ';'
);
fix.push(`
-- Keep the identity counter ahead of the highest fixture id.
select setval(pg_get_serial_sequence('fixtures', 'id'),
  greatest(${fixtureRows.length}, (select coalesce(max(id), 0) from fixtures)));

commit;
`);

writeFileSync(join(__dirname, '..', 'supabase-fix-fixtures.sql'), fix.join('\n'));
console.log(`Wrote supabase-fix-fixtures.sql (non-destructive: ${fixtureRows.length} group fixtures, picks/draft preserved).`);
